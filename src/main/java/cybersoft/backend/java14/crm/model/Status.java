package cybersoft.backend.java14.crm.model;

public class Status {
	//Attribute
	private int id;
	private String name;
	
	//Constructor
	public Status() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
